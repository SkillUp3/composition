public class Resolution {
    private int mWidth;
    private int mHeight;

    public Resolution(int width, int height) {
        mWidth = width;
        mHeight = height;
    }

    public int getWidth() {
        return mWidth;
    }

    public int getHeight() {
        return mHeight;
    }

}
